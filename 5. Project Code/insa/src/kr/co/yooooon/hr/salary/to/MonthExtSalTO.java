package kr.co.yooooon.hr.salary.to;

public class MonthExtSalTO {
	private String monthExtSalCode,
	applyYearMonth,
	empCode,
	extSalCode,
	extSalName,
	price;

	public String getMonthExtSalCode() {
		return monthExtSalCode;
	}

	public void setMonthExtSalCode(String monthExtSalCode) {
		this.monthExtSalCode = monthExtSalCode;
	}

	public String getApplyYearMonth() {
		return applyYearMonth;
	}

	public void setApplyYearMonth(String applyYearMonth) {
		this.applyYearMonth = applyYearMonth;
	}

	public String getEmpCode() {
		return empCode;
	}

	public void setEmpCode(String empCode) {
		this.empCode = empCode;
	}

	public String getExtSalCode() {
		return extSalCode;
	}

	public void setExtSalCode(String extSalCode) {
		this.extSalCode = extSalCode;
	}

	public String getExtSalName() {
		return extSalName;
	}

	public void setExtSalName(String extSalName) {
		this.extSalName = extSalName;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}
}
